#to return a range of characters by using the slice syntax

text1 = "Hello World"
print(text1[2:5]) #Get characters from pos 2 to pos 5 (not included)
print(text1[:5]) #Get characters from start to pos 5 (not included)
print(text1[2:]) #Get characters from pos 2 to the end
print(text1[-5:-2]) #Get characters from pos -5 to pos -2 (not included)