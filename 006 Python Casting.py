#Used to specify a type on to a variable
#int    constructs an integer number
#float  constructs a float number
#str    constructs a string

x1 = int(1)     #will be 1
x2 = int(2.8)   #will be 2
x3 = int("3")   #will be 3

y1 = float(1)       #will be 1.0
y2 = float(2.8)     #will be 2.8
y3 = float("2.8")   #will be 2.8

print(int(2.8))     #will be 2
print(float(2.8))   #will be 2.8