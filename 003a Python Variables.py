x = 4       #This is int or integer
y = "Sally" #This is str or string

print(x)
print(y)

#--------------CASTING---------------------------------
#to specify the data type of a variable
a = str(3)      #will be '3'
b = int(3)      #will be 3
c = float(3)    #will be 3.0

print(a)
print(b)
print(c)

#--------------GET THE TYPE---------------------------------
print(type(a))
print(type(b))
print(type(c))

#--------------STRING WITH QUOTE OR DOUBLE QUOTE-----------
d = 'John'
e = "John" #is the same with ' '
print(d)
print(e)
print(type(d))
print(type(e))

#--------------VARIABLES ARE CASE SENSITIVE-----------
D = 'Alice'
E = "Alice"
print(D)
print(E)
print(type(D))
print(type(E))